class ProjectData {
  final String image;
  final String title;
  final double height;
  final double size;
  final String values;
  final String message;
  final String url;
  final bool isButtonEnabled;
  final String buttonText;

  ProjectData({
    required this.image,
    required this.title,
    required this.height,
    required this.size,
    required this.values,
    required this.message,
    required this.url,
    required this.isButtonEnabled,
    required this.buttonText
  });
}